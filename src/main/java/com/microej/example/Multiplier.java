package com.microej.example;

/**
 * A simple class that implements a function that multiplies a number by two.
 */
public class Multiplier {

	/**
	 * A simple function that multiplies a number by two.
	 *
	 * @param n The input number
	 * @return The value of this number multiplied by two
	 */
	public static int multiplyByTwo(int n) {
		return 2 * n;
	}

}
